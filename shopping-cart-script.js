// Funcția care încarcă coșul din localStorage
function loadCartFromLocalStorage() {
  const cartData = localStorage.getItem('cosCumparaturi');
  if (cartData) {
    cosCumparaturi = JSON.parse(cartData); // Parsează datele salvate
  }
}

// Funcția pentru a afișa notificarea
function showNotification(message) {
  const notification = document.createElement('div');
  notification.className = 'notification';
  notification.innerText = message;

  // Stiluri pentru notificare
  notification.style.position = 'fixed';
  notification.style.bottom = `${20 + notificationCount * 60}px`; // Ajustează poziția pe verticală
  notification.style.right = '20px';
  notification.style.backgroundColor = '#4caf50'; // Verde
  notification.style.color = 'white';
  notification.style.padding = '10px 20px';
  notification.style.borderRadius = '5px';
  notification.style.zIndex = '1000'; // Asigură-te că notificarea este deasupra altor elemente

  document.body.appendChild(notification);
  notificationCount++; // Incrementăm numărul de notificări afișate

  // Dispare după 3 secunde
  setTimeout(() => {
    notification.remove();
    notificationCount--; // Decrementăm numărul de notificări după dispariție
    adjustNotificationPositions(); // Ajustăm pozițiile notificărilor rămase
  }, 3000);
}

// Funcția pentru a ajusta pozițiile notificărilor rămase
function adjustNotificationPositions() {
  const notifications = document.querySelectorAll('.notification');
  notifications.forEach((notif, index) => {
    notif.style.bottom = `${20 + index * 60}px`; // Recalculează poziția fiecărei notificări
  });
}
