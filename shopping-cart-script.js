let categorieProduse = 'Nutritie';
let cosCumparaturi = [];
const itemsPerPage = 15;
let currentPage = 1;
let notificationCount = 0; // Variabilă pentru a număra notificările

function createProductMatrix(array, itemsPerRow) {
  const matrix = [];
  for (let i = 0; i < array.length; i += itemsPerRow) {
    matrix.push(array.slice(i, i + itemsPerRow));
  }
  return matrix;
}

function generateProductCards() {
  // Folosește un container dinamic în funcție de categorie

  const productContainer = document.getElementById(
    `product-container-${categorieProduse.toLowerCase().replace(/ /g, '-')}`
  );

  if (!productContainer) {
    console.error('Elementul product-container nu a fost găsit.');
    return;
  }
  productContainer.innerHTML = '';

  const totalPages = Math.ceil(
    arrayProduse[categorieProduse].length / itemsPerPage
  );
  const startIndex = (currentPage - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const productsToShow = arrayProduse[categorieProduse].slice(
    startIndex,
    endIndex
  );

  productsToShow.forEach((produs, index) => {
    const card = document.createElement('div');
    card.className = 'card';
    card.id = startIndex + index + 1;
    card.innerHTML = `
      <img src="${produs.image}" alt="${produs.name}" style="width: 100%" />
      <h1>${produs.name}</h1>
      <p class="price">${produs.price} RON</p>
      <p class="card-description">${produs.description}</p>
      <button onclick="addToCart('${produs.name}', '${produs.price}', '${produs.command_number}')">Adaugă în coș</button>
    `;
    productContainer.appendChild(card);
  });

  const emptyCardsCount = itemsPerPage - productsToShow.length;
  for (let i = 0; i < emptyCardsCount; i++) {
    const emptyCard = document.createElement('div');
    emptyCard.className = 'card empty-card';
    productContainer.appendChild(emptyCard);
  }

  updatePaginationButtons(totalPages);
}

function updatePaginationButtons(totalPages) {
  document.querySelector('.page-numbers-button-prev').disabled =
    currentPage === 1;
  document.querySelector('.page-numbers-button-next').disabled =
    currentPage === totalPages;
}

function backPage() {
  if (currentPage > 1) {
    currentPage--;
    generateProductCards();
  }
}

function nextPage() {
  const totalPages = Math.ceil(
    arrayProduse[categorieProduse].length / itemsPerPage
  );
  if (currentPage < totalPages) {
    currentPage++;
    generateProductCards();
  }
}

function openCategory(evt, categoryName) {
  let i, tabcontent, tablinks;
  categorieProduse = categoryName;
  console.log(categoryName);

  tabcontent = document.getElementsByClassName('tabcontent');
  for (i = 0; i < tabcontent.length; i++) {
    tabcontent[i].style.display = 'none';
  }

  tablinks = document.getElementsByClassName('tab-opener');
  for (i = 0; i < tablinks.length; i++) {
    tablinks[i].className = tablinks[i].className.replace(' active', '');
  }

  document.getElementById(categoryName).style.display = 'block';
  evt.currentTarget.className += ' active';

  currentPage = 1;
  generateProductCards();
}

