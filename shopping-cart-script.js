let categorieProduse = 'Nutritie';
let cosCumparaturi = [];
const itemsPerPage = 15;
let currentPage = 1;
let notificationCount = 0; // Variabilă pentru a număra notificările

function createProductMatrix(array, itemsPerRow) {
  const matrix = [];
  for (let i = 0; i < array.length; i += itemsPerRow) {
    matrix.push(array.slice(i, i + itemsPerRow));
  }
  return matrix;
}

function generateProductCards() {
  // Folosește un container dinamic în funcție de categorie

  const productContainer = document.getElementById(
    `product-container-${categorieProduse.toLowerCase().replace(/ /g, '-')}`
  );

  if (!productContainer) {
    console.error('Elementul product-container nu a fost găsit.');
    return;
  }
  productContainer.innerHTML = '';

  const totalPages = Math.ceil(
    arrayProduse[categorieProduse].length / itemsPerPage
  );
  const startIndex = (currentPage - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const productsToShow = arrayProduse[categorieProduse].slice(
    startIndex,
    endIndex
  );

  productsToShow.forEach((produs, index) => {
    const card = document.createElement('div');
    card.className = 'card';
    card.id = startIndex + index + 1;
    card.innerHTML = `
      <img src="${produs.image}" alt="${produs.name}" style="width: 100%" />
      <div class="overflow-class">
        <h1>${produs.name}</h1>
        <p class="price">${produs.price} RON</p>
        <p class="card-description">${produs.description}</p>
      </div>
      <button
        onclick="addToCart('${produs.name}', '${produs.price}', '${produs.command_number}')"
      >
        Adaugă în coș
      </button>
        `;
    productContainer.appendChild(card);
  });

  const emptyCardsCount = itemsPerPage - productsToShow.length;
  for (let i = 0; i < emptyCardsCount; i++) {
    const emptyCard = document.createElement('div');
    emptyCard.className = 'card empty-card';
    productContainer.appendChild(emptyCard);
  }

  updatePaginationButtons(totalPages);
}

function updatePaginationButtons(totalPages) {
  document.querySelector('.page-numbers-button-prev').disabled =
    currentPage === 1;
  document.querySelector('.page-numbers-button-next').disabled =
    currentPage === totalPages;
}

function backPage() {
  if (currentPage > 1) {
    currentPage--;
    generateProductCards();
  }
}

function nextPage() {
  const totalPages = Math.ceil(
    arrayProduse[categorieProduse].length / itemsPerPage
  );
  if (currentPage < totalPages) {
    currentPage++;
    generateProductCards();
  }
}

function openCategory(evt, categoryName) {
  let i, tabcontent, tablinks;
  categorieProduse = categoryName;
  console.log(categoryName);

  tabcontent = document.getElementsByClassName('tabcontent');
  for (i = 0; i < tabcontent.length; i++) {
    tabcontent[i].style.display = 'none';
  }

  tablinks = document.getElementsByClassName('tab-opener');
  for (i = 0; i < tablinks.length; i++) {
    tablinks[i].className = tablinks[i].className.replace(' active', '');
  }

  document.getElementById(categoryName).style.display = 'block';
  evt.currentTarget.className += ' active';

  currentPage = 1;
  generateProductCards();
}

document.addEventListener('DOMContentLoaded', function () {
  generateProductCards();
});

function addToCart(name, price, command_number) {
  cosCumparaturi.push([name, price, command_number]);
  saveCartToLocalStorage(); // Salvează coșul după adăugare
  updateCart(); // Actualizăm lista dacă modalul este deschis
  showNotification(`Produsul "${name}" a fost adăugat în coș!`); // Afișează notificarea
}

function removeFromCart(index) {
  // Elimină elementul din coș folosind indexul
  cosCumparaturi.splice(index, 1);
  saveCartToLocalStorage(); // Salvează coșul după ștergere
  updateCart(); // Actualizează coșul după ștergere
}

// Funcția care salvează coșul în localStorage
function saveCartToLocalStorage() {
  localStorage.setItem('cosCumparaturi', JSON.stringify(cosCumparaturi));
}

function showModal(name, price) {
  const modal = document.getElementById('cart-modal');
  modal.style.display = 'block'; // Afișează modalul

  // Modifică doar mesajul din modal, păstrează restul conținutului
  modal.querySelector('.cos-cumparaturi-titlu').innerText = 'Cos Cumparaturi';

  // Adaugă detaliile produsului în modal
  const productDetails = `
    <p><strong>${name}</strong></p>
    <p>Preț: ${price} RON</p>
    <ul id="cart-modal-list"></ul>
  `;

  // Verifică dacă detaliile produsului sunt deja în modal
  if (!modal.querySelector('.product-details')) {
    const detailsDiv = document.createElement('div');
    detailsDiv.classList.add('product-details');
    detailsDiv.innerHTML = productDetails;
    modal.insertBefore(detailsDiv, modal.querySelector('.button-cumpara'));
  } else {
    // Actualizează detaliile produsului
    modal.querySelector('.product-details').innerHTML = productDetails;
  }

  // Afișează butonul de cumpărare
  const buyButton = modal.querySelector('.button-cumpara');
  buyButton.style.display = 'block'; // Asigură-te că butonul este vizibil

  updateCart(); // Actualizăm coșul
}

function openCart() {
  const cartModal = document.getElementById('cart-modal');
  cartModal.style.display = 'block'; // Afișează modalul
  updateCart(); // Actualizăm lista de produse
}

function updateCart() {
  const cartItemsList = document.getElementById('cart-modal-list');
  cartItemsList.innerHTML = ''; // Golește lista de produse

  let totalPrice = 0; // Variabilă pentru suma totală

  // Adaugă produsele în coș
  cosCumparaturi.forEach((item, index) => {
    const price = parseFloat(item[1]); // Convertim prețul la număr
    totalPrice += price; // Adăugăm prețul la suma totală

    const li = document.createElement('li');
    li.className = 'cart-item'; // Adaugă o clasă pentru stilizare
    li.innerHTML = `
      <span onclick="removeFromCart(${index})" style="cursor: pointer; color: red; font-weight: bold;">X</span>
      <div>
        <strong>Name:</strong> ${item[0]}<br>
        <strong>Price:</strong> ${price} RON<br>
        <strong>Command:</strong> ${item[2]}<br><br>
      </div>
    `;
    cartItemsList.appendChild(li);
  });

  // Actualizăm suma totală în elementul cu id-ul 'pret-total'
  const totalElement = document.getElementById('pret-total');
  totalElement.innerHTML = `Total: ${totalPrice.toFixed(2)} RON`; // Formatează la 2 zecimale
}

function closeCart() {
  document.getElementById('cart-modal').style.display = 'none'; // Ascunde modalul
}
function removeFromCart(index) {
  // Elimină elementul din coș folosind indexul
  cosCumparaturi.splice(index, 1);
  saveCartToLocalStorage(); // Salvează coșul după ștergere
  updateCart(); // Actualizează coșul după ștergere
}

